#include <stddef.h>

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	int		i;
	
	i = 0;
	while(s1[i] != '\0')
		i++;
	while (n > 0)
	{
		s1[i] = *s2;
		i++;
		s2++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
