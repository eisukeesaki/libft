#include "libft.h"

size_t	ft_words(char const *s, char d)
{
	size_t	w;

	w = 0;
	while (*s == d && *s)
		s++;
	while (*s)
	{
		while (*s != d && *s)
			s++;
		w++;
		while (*s == d && *s)
			s++;
	}
	return (w);
}
