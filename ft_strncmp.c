#include <stddef.h>

int		ft_strncmp(const char *s1, const char *s2, size_t len)
{
	while (len > 0)
	{
		if (*s1 == *s2)
		{
			s1++;
			s2++;
			len--;
		}
		else if (*s1 > *s2)
			return (*s1 - *s2);
		else if (*s1 < *s2)
			return (*s1 - *s2);
	}
	return (0);
}
